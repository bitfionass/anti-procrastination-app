  /**
   * 拆解任务
   * @param {string} input - 用户输入的任务描述
   * @param {string} difficulty - 难度：easy/medium/hard
   * @returns {{ title: string, steps: Array<{text: string, difficulty: string}>, encouragement: string }}
   */
  function decompose(input, difficulty = 'easy') {
    const title = input.trim();
    if (!title) return null;

    // 匹配模板
    let matchedTemplate = null;
    for (const [key, template] of Object.entries(templates)) {
      if (template.keywords.some(kw => title.includes(kw))) {
        matchedTemplate = template;
        break;
      }
    }

    // 获取步骤
    const rawSteps = matchedTemplate
      ? matchedTemplate.steps[difficulty] || matchedTemplate.steps.easy
      : genericSteps[difficulty] || genericSteps.easy;

    // 构造步骤对象
    const steps = rawSteps.map((text, i) => ({
      id: i + 1,
      text,
      done: false,
      difficulty
    }));

    // 随机鼓励语
    const encouragement = encouragements[Math.floor(Math.random() * encouragements.length)];

    return { title, steps, encouragement };
  }

